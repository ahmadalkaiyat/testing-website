<?php

namespace App\Http\Controllers;

use App\Branch;
use App\Status;
use Illuminate\Http\Request;
use Illuminate\Support\Facades\Auth;

class AdminBranchesController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $branches =Branch::all();
        return view('admin.branches.index',compact('branches'));
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        $status = Status::pluck('name','id')->all();

        return view('admin.branches.create' ,compact('status'));
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $input  = $request->all();

        $input['user_id']=Auth::user()->id;

        Branch::create($input);

        return redirect('admin/branches');
    }

    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        //
    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        $branch = Branch::findOrFail($id);  // finding the post where we are sending the ID

        $status =Status::pluck('name','id');

        return view('admin.branches.edit',compact('branch','status'));
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $branch = Branch::findOrFail($id);
        $input =$request->all();



        $branch->update($input)  ;  // new way to update in one line , check update users for old way !

        return redirect('/admin/branches');
    }

